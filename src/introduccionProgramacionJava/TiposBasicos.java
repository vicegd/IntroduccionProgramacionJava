package introduccionProgramacionJava;

public class TiposBasicos { 
	public static void main(String[] args) {
		int numero_entero = 10;
		float numero_decimal = 4.2f;
		char caracter = 'A';
		boolean valor_logico = true;
		
		System.out.printf("El valor entero es: %d \n", numero_entero);
		System.out.printf("El valor decimal es: %f \n", numero_decimal);
		System.out.printf("El caracter es: %c \n", caracter);
		System.out.printf("El valor lógico es: %b \n", valor_logico);
	}
}




